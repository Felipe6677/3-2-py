def comparar_carros(cor_carro1, cor_carro2):
    if cor_carro1 == cor_carro2:
        print(f"Os dois carros têm a mesma cor: {cor_carro1}.")
    else:
        print(f"Os carros têm cores diferentes: um é {cor_carro1} e o outro é {cor_carro2}.")

comparar_carros("vermelho", "azul")
comparar_carros("amarelo", "amarelo")

